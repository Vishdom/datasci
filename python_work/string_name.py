name = "ada lovelace"
print(name.title())

#To change the case of a string, you can use the upper() and lower() methods.
print(name.upper())
print(name.lower())

#You can combine strings using the + operator to create a new string.
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
print(full_name)
print("Hello, " + full_name.title() + "!")
